# Escape Sequence Characters in Python

print("1. Newline: Hello\\nWorld")
print("Hello\nWorld")

print("2. Backslash: Hello\\\\World")
print("Hello\\World")

print("3. Single Quote: It\\'s a good day")
print('It\'s a good day')

print("4. Double Quote: \"Hello World\"")
print("\"Hello World\"")

print("5. Tab: Hello\\tWorld")
print("Hello\tWorld")

print("6. Carriage Return: Hello\\rWorld")
print("Hello\rWorld")

print("7. Backspace: Hello\\bWorld")
print("Hello\bWorld")

print("8. Form Feed: Hello\\fWorld")
print("Hello\fWorld")

print("9. Octal Value: Hello\\110World")
print("Hello\110World")

print("10. Hex Value: Hello\\x48World")
print("Hello\x48World")